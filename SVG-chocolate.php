<h1>Invertir en cacao y chocolate venezolano</h1>

<p>Muchos expertos sostienen que el cacao venezolano es el mejor del mundo. 
Quizás tal afirmación haga poco honor al trabajo que se ha venido realizando 
en Ecuador, Perú y Colombia para conseguir el título de país con el mejor 
cacao del mundo. Pero la verdad es que Venezuela, en donde a pesar que se 
han descuidado los procesos para obtener un cacao digno de ser llamado el 
mejor, la gran variedad de cacao fino de aroma venezolano no tiene parangón en 
ninguna otra parte del planeta.</p> 

<p>Venezuela fue históricamente un productor de cacao de aroma fino. Pero 
el boom de la industria petrolera por un lado y las erradas políticas del 
país por el otro, sacaron a Venezuela del mapa como país productor de cacao.</p>

<p>Sin embargo, el inmenso potencial de Venezuela en ese sector todavía está 
allí latente. En las regiones donde se cultiva el cacao aún se mantienen las 
antiguas tradiciones que han garantizado de cierta manera la merecida fama del 
cacao y el chocolate venezolano.</p>

<p>Además, en un contexto en donde el producto más valioso y precioso ya no es 
el barril de petroleo, el cacao se alza como la opción clara para la salida de 
la pobreza de centenares de familias: Un kilo de chocolate venezolano en el 
mercado europeo tiene más valor que un barril de petroleo.</p>

<p>Por otra parte, hoy día Venezuela dispone, en este momento, de la mano de 
obra más económica del mundo. Invertir en la industria del chocolate de Venezuela 
quizá no sea el negocio más delicioso, para quien no es muy amante del chocolate, 
pero para cualquiera es seguramente el mejor negocio que se puede hacer tanto si 
se mira obtener dividendos como la satisfacción de alcanzar metas de índole social.</p>

